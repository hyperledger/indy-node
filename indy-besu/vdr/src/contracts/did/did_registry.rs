use log::{debug, info};

use crate::{
    client::{
        Address, ContractParam, LedgerClient, Transaction, TransactionBuilder, TransactionParser,
        TransactionType,
    },
    contracts::did::types::did_doc::{DidDocument, DidDocumentWithMeta},
    error::VdrResult,
    DID,
};

/// IndyDidRegistry contract methods
pub struct IndyDidRegistry;

impl IndyDidRegistry {
    const CONTRACT_NAME: &'static str = "IndyDidRegistry";
    const METHOD_CREATE_DID: &'static str = "createDid";
    const METHOD_UPDATE_DID: &'static str = "updateDid";
    const METHOD_DEACTIVATE_DID: &'static str = "deactivateDid";
    const METHOD_RESOLVE_DID: &'static str = "resolveDid";

    /// Build transaction to execute IndyDidRegistry.createDid contract method to create a new DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did_doc` DID Document matching to the specification: https://www.w3.org/TR/did-core/
    ///
    /// # Returns
    /// Write transaction to sign and submit
    pub fn build_create_did_transaction(
        client: &LedgerClient,
        from: &Address,
        did_doc: &DidDocument,
    ) -> VdrResult<Transaction> {
        debug!(
            "{} txn build has started. Sender: {}, DidDocument: {:?}",
            Self::METHOD_CREATE_DID,
            from.value(),
            did_doc
        );

        let transaction = TransactionBuilder::new()
            .set_contract(Self::CONTRACT_NAME)
            .set_method(Self::METHOD_CREATE_DID)
            .add_param(did_doc.clone().into())
            .set_type(TransactionType::Write)
            .set_from(from)
            .build(client);

        info!(
            "{} txn build has finished. Result: {:?}",
            Self::METHOD_CREATE_DID,
            transaction
        );

        transaction
    }

    /// Build transaction to execute IndyDidRegistry.updateDid contract method to update DID document for an existing DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did_doc` new DID Document matching to the specification: https://www.w3.org/TR/did-core/
    ///
    /// # Returns
    /// Write transaction to sign and submit
    pub fn build_update_did_transaction(
        client: &LedgerClient,
        from: &Address,
        did_doc: &DidDocument,
    ) -> VdrResult<Transaction> {
        debug!(
            "{} txn build has started. Sender: {}, DidDocument: {:?}",
            Self::METHOD_UPDATE_DID,
            from.value(),
            did_doc
        );

        let transaction = TransactionBuilder::new()
            .set_contract(Self::CONTRACT_NAME)
            .set_method(Self::METHOD_UPDATE_DID)
            .add_param(did_doc.clone().into())
            .set_type(TransactionType::Write)
            .set_from(from)
            .build(client);

        info!(
            "{} txn build has finished. Result: {:?}",
            Self::METHOD_UPDATE_DID,
            transaction
        );

        transaction
    }

    /// Build transaction to execute IndyDidRegistry.deactivateDid contract method to deactivate an existing DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did` DID to deactivate
    ///
    /// # Returns
    /// Write transaction to sign and submit
    pub fn build_deactivate_did_transaction(
        client: &LedgerClient,
        from: &Address,
        did: &DID,
    ) -> VdrResult<Transaction> {
        debug!(
            "{} txn build has started. Sender: {}, Did: {:?}",
            Self::METHOD_DEACTIVATE_DID,
            from.value(),
            did
        );

        let transaction = TransactionBuilder::new()
            .set_contract(Self::CONTRACT_NAME)
            .set_method(Self::METHOD_DEACTIVATE_DID)
            .add_param(ContractParam::String(did.value().to_string()))
            .set_type(TransactionType::Write)
            .set_from(from)
            .build(client);

        info!(
            "{} txn build has finished. Result: {:?}",
            Self::METHOD_DEACTIVATE_DID,
            transaction
        );

        transaction
    }

    /// Build transaction to execute IndyDidRegistry.resolveDid contract method to receive a DID Document associated with the DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `did` target DID to receive DID Document
    ///
    /// # Returns
    /// Read transaction to submit
    pub fn build_resolve_did_transaction(
        client: &LedgerClient,
        did: &DID,
    ) -> VdrResult<Transaction> {
        debug!(
            "{} txn build has started. Did: {:?}",
            Self::METHOD_RESOLVE_DID,
            did
        );

        let transaction = TransactionBuilder::new()
            .set_contract(Self::CONTRACT_NAME)
            .set_method(Self::METHOD_RESOLVE_DID)
            .add_param(ContractParam::String(did.value().to_string()))
            .set_type(TransactionType::Read)
            .build(client);

        info!(
            "{} txn build has finished. Result: {:?}",
            Self::METHOD_RESOLVE_DID,
            transaction
        );

        transaction
    }

    /// Parse the result of execution IndyDidRegistry.resolveDid contract method to receive a DID Document associated with the DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `bytes` result bytes returned from the ledger
    ///
    /// # Returns
    /// parsed DID Document
    pub fn parse_resolve_did_result(client: &LedgerClient, bytes: &[u8]) -> VdrResult<DidDocument> {
        debug!(
            "{} result parse has started. Bytes to parse: {:?}",
            Self::METHOD_RESOLVE_DID,
            bytes
        );

        let result = TransactionParser::new()
            .set_contract(Self::CONTRACT_NAME)
            .set_method(Self::METHOD_RESOLVE_DID)
            .parse::<DidDocumentWithMeta>(client, bytes)
            .map(|did_with_meta| did_with_meta.document);

        info!(
            "{} result parse has finished. Result: {:?}",
            Self::METHOD_RESOLVE_DID,
            result
        );

        result
    }

    /// Single step function executing IndyDidRegistry.createDid smart contract method to create a new DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did_doc` DID Document matching to the specification: https://www.w3.org/TR/did-core/
    ///
    /// # Returns
    /// receipt of executed transaction
    pub async fn create_did(
        client: &LedgerClient,
        from: &Address,
        did_doc: &DidDocument,
    ) -> VdrResult<String> {
        debug!(
            "{} process has started. Sender: {}, DidDocument: {:?}",
            Self::METHOD_CREATE_DID,
            from.value(),
            did_doc
        );

        let transaction = Self::build_create_did_transaction(client, from, did_doc)?;
        let receipt = client.sign_and_submit(&transaction).await;

        info!(
            "{} process has finished. Result: {:?}",
            Self::METHOD_CREATE_DID,
            receipt
        );

        receipt
    }

    /// Single step function executing IndyDidRegistry.updateDid smart contract method to update DID Document for an existing DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did_doc` DID Document matching to the specification: https://www.w3.org/TR/did-core/
    ///
    /// # Returns
    /// receipt of executed transaction
    pub async fn update_did(
        client: &LedgerClient,
        from: &Address,
        did_doc: &DidDocument,
    ) -> VdrResult<String> {
        debug!(
            "{} process has started. Sender: {}, DidDocument: {:?}",
            Self::METHOD_UPDATE_DID,
            from.value(),
            did_doc
        );

        let transaction = Self::build_update_did_transaction(client, from, did_doc)?;
        let receipt = client.sign_and_submit(&transaction).await;

        info!(
            "{} process has finished. Result: {:?}",
            Self::METHOD_UPDATE_DID,
            receipt
        );

        receipt
    }

    /// Single step function executing IndyDidRegistry.deactivateDid smart contract method to deactivate as existing DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did` DID to deactivate
    ///
    /// # Returns
    /// receipt of executed transaction
    pub async fn deactivate_did(
        client: &LedgerClient,
        from: &Address,
        did: &DID,
    ) -> VdrResult<String> {
        debug!(
            "{} process has started. Sender: {}, DID: {:?}",
            Self::METHOD_DEACTIVATE_DID,
            from.value(),
            did
        );

        let transaction = Self::build_deactivate_did_transaction(client, from, did)?;
        let receipt = client.sign_and_submit(&transaction).await;

        info!(
            "{} process has finished. Result: {:?}",
            Self::METHOD_DEACTIVATE_DID,
            receipt
        );

        receipt
    }

    /// Single step function executing IndyDidRegistry.resolveDid smart contract method to resolve DID Document for an existing DID
    ///
    /// # Params
    /// - `client` client connected to the network where contract will be executed
    /// - `from` transaction sender account address
    /// - `did` target DID to receive DID Document
    ///
    /// # Returns
    /// resolved DID Document
    pub async fn resolve_did(client: &LedgerClient, did: &DID) -> VdrResult<DidDocument> {
        debug!(
            "{} process has started. DID: {:?}",
            Self::METHOD_RESOLVE_DID,
            did
        );

        let transaction = Self::build_resolve_did_transaction(client, did)?;
        let result = client.submit_transaction(&transaction).await?;
        let parsed_result = Self::parse_resolve_did_result(client, &result);

        info!(
            "{} process has finished. Result: {:?}",
            Self::METHOD_RESOLVE_DID,
            parsed_result
        );

        parsed_result
    }
}

